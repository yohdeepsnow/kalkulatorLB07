def penambahan(a,b):
	total = a+b
	return total

def pengurangan(a,b):
	total = a-b
	return total

def perkalian(a,b):
	total = a*b
	return total
def pembagian(a,b):
	total = a/b
	return total
def pangkatdua(a):
	total = a*a
	return total

def main():
	print (penambahan(10,5))
	print (pengurangan(10,5))
	print (perkalian(10,5))
	print (pembagian(10,5))
	print (pangkatdua(5))
main()
